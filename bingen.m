function Y = bingen(L)
%g产生长度为L的1/0序列
%*************************************************************************************
%         L ---> number of random symbols               
%***********************************************************************************

    tmep = rand(1,L);          %生成长度为L，大小在0~1内的随机序列
    tmep(tmep<0.5) = 0;    %抽样判决，小于0.5为-1，反之为1
    tmep(tmep >=0.5) = 1;
    Y = tmep;
end

